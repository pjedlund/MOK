/**
 * Regenerate the YAML front matter in `docs/DESIGN.md` from the design
 * tokens in `src/_data/designTokens/*.json`. The markdown body below the
 * front matter is preserved verbatim — humans own the prose; this script
 * owns the tokens.
 *
 * The generated front matter conforms to the alpha DESIGN.md spec:
 *   https://github.com/anthropic-ai (see docs/DESIGN.md and spec.md)
 *
 * Workflow:
 *   1. Edit any file in src/_data/designTokens/
 *      (if you edited colorsBase.json, run `npm run colors` first)
 *   2. Run `npm run design:md`  (or `npm run penpot:tokens` which chains both)
 *   3. Review the diff. The markdown body below the second `---` is untouched.
 *
 * Mapping rules:
 *   - Semantic roles (primary/secondary/tertiary/neutral) follow the
 *     SEMANTIC_ROLES map at the top of this file. Edit it to rebrand.
 *   - Surface / on-surface tokens are resolved from semanticColors.json
 *     (which itself references palette tokens). The dark theme is emitted
 *     with a `-dark` suffix (the spec has no native theming concept; this
 *     uses the spec's "unknown token name → accept if value is valid" rule).
 *   - Typography composites are resolved from typography.json by following
 *     the {font.*} references into fonts/textWeights/textSizes/textLeading.
 *     Font sizes collapse the Utopia {min,max} pair to the max in px,
 *     matching how Penpot tokens are emitted.
 *   - Components are defined inline in the COMPONENTS map below — they
 *     wire up token references rather than being inferred from JSON.
 */

import {mkdir, readFile, writeFile} from 'node:fs/promises';
import {dirname, resolve} from 'node:path';
import {fileURLToPath} from 'node:url';
import yaml from 'js-yaml';

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '../../..');
const TOKENS_DIR = resolve(REPO_ROOT, 'src/_data/designTokens');
const OUTPUT_PATH = resolve(REPO_ROOT, 'docs/DESIGN.md');

// =============================================================================
// Configuration — edit when the design system's semantic mapping changes.
// =============================================================================

const META = {
	version: 'alpha',
	name: 'JEDEE',
	description:
		'Design system for johanedlund.se — a personal indieweb site by Johan Edlund. ' +
		'Editorial in tone, fluid by construction (Utopia type and space scales), ' +
		'warm-neutral in palette, with a single vibrant orange as the accent driver. ' +
		'Tokens are sourced from src/_data/designTokens/*.json; this file is a human- ' +
		'and agent-readable view of the same system. Front matter is generated by ' +
		'src/_config/setup/build-design-md.js — edit the JSON tokens, not the YAML.',
};

// Maps DESIGN.md semantic role names → palette short names in colors.json.
// "blue" / "green" / "red" reach the top-level keys; "gray-500" / "orange-500"
// reach a nested shade; "base-darkest" reaches a hyphenated top-level key.
const SEMANTIC_ROLES = {
	primary: 'base-darkest',
	secondary: 'orange-500',
	tertiary: 'blue',
	neutral: 'base-lightest',
};

// Surface / on-surface mapping — outKey ← key in semanticColors.json themes.
const SURFACE_KEYS = [
	['surface', 'bg'],
	['surface-accent', 'bg-accent'],
	['surface-accent-2', 'bg-accent-2'],
	['on-surface', 'text'],
	['on-surface-accent', 'text-accent'],
	['headline', 'headline'],
];

// Component map — values are token references resolved by DESIGN.md consumers.
const COMPONENTS = {
	body: {
		backgroundColor: '{colors.surface}',
		textColor: '{colors.on-surface}',
		typography: '{typography.body}',
	},
	'body-dark': {
		backgroundColor: '{colors.surface-dark}',
		textColor: '{colors.on-surface-dark}',
	},
	'heading-1': {
		typography: '{typography.heading-1}',
		textColor: '{colors.headline}',
	},
	'heading-2': {
		typography: '{typography.heading-2}',
		textColor: '{colors.headline}',
	},
	'heading-3': {
		typography: '{typography.heading-3}',
		textColor: '{colors.headline}',
	},
	link: {
		textColor: '{colors.on-surface}',
		underlineColor: '{colors.orange-600}',
	},
	'link-hover': {
		textColor: '{colors.accent-orange}',
		underlineColor: '{colors.accent-red-subdued}',
	},
	blockquote: {
		backgroundColor: '{colors.surface}',
		textColor: '{colors.on-surface}',
		typography: '{typography.blockquote}',
		borderInlineStartColor: '{colors.accent-orange}',
		padding: '{spacing.m-l}',
	},
	'code-block': {
		backgroundColor: '{colors.surface-accent}',
		textColor: '{colors.on-surface}',
		typography: '{typography.code}',
		rounded: '{rounded.sm}',
		padding: '{spacing.s}',
	},
	hr: {
		backgroundColor: '{colors.surface-accent-2}',
		height: '1px',
		width: '10%',
	},
	figcaption: {
		typography: '{typography.caption}',
		textColor: '{colors.on-surface-accent}',
	},
	'focus-ring': {
		outlineColor: '{colors.on-surface}',
		outlineWidth: '3px',
	},
	selection: {
		backgroundColor: '{colors.on-surface}',
		textColor: '{colors.surface}',
	},
};

// Fallback markdown body, written only when DESIGN.md does not yet exist.
const DEFAULT_BODY = `# ${META.name} Design System

## Overview

_Add brand-and-style narrative here._

## Colors

## Typography

## Layout

## Elevation & Depth

## Shapes

## Components

## Do's and Don'ts
`;

// =============================================================================
// Helpers
// =============================================================================

async function readJSON(name) {
	return JSON.parse(await readFile(resolve(TOKENS_DIR, name), 'utf8'));
}

function fluidMaxToPx(v) {
	if (v && typeof v === 'object' && 'max' in v) return `${v.max}px`;
	if (typeof v === 'number') return `${v}px`;
	return v;
}

// Resolve a "{color.foo.bar}" reference against colors.json. Tries nested
// lookup first (gray.900), then falls back to a hyphen-joined top-level key
// (base.lightest → base-lightest).
function resolveColorRef(ref, colors) {
	const m = String(ref).match(/^\{([^}]+)\}$/);
	if (!m) return ref;
	const path = m[1];
	if (!path.startsWith('color.')) return null;
	const parts = path.slice('color.'.length).split('.');
	let cur = colors;
	for (const p of parts) {
		if (cur && typeof cur === 'object' && p in cur) cur = cur[p];
		else {
			cur = null;
			break;
		}
	}
	if (cur && cur.$value) return cur.$value;
	const hyphen = parts.join('-');
	if (colors[hyphen]?.$value) return colors[hyphen].$value;
	return null;
}

// Look up a hex by short palette name (e.g. "gray-100", "base-darkest", "blue").
function paletteHex(name, colors) {
	if (colors[name]?.$value) return colors[name].$value;
	const m = name.match(/^([a-z]+)-(.+)$/);
	if (m && colors[m[1]]?.[m[2]]?.$value) return colors[m[1]][m[2]].$value;
	return null;
}

// =============================================================================
// Build the front-matter data object
// =============================================================================

async function build() {
	const [colors, fonts, spacing, textSizes, textLeading, textWeights, borderRadius, semanticColors, typographyJson] = await Promise.all([
		readJSON('colors.json'),
		readJSON('fonts.json'),
		readJSON('spacing.json'),
		readJSON('textSizes.json'),
		readJSON('textLeading.json'),
		readJSON('textWeights.json'),
		readJSON('borderRadius.json'),
		readJSON('semanticColors.json'),
		readJSON('typography.json'),
	]);

	const out = {
		version: META.version,
		name: META.name,
		description: META.description,
		colors: {},
		typography: {},
		rounded: {},
		spacing: {},
		components: {},
	};

	// ---- colors: semantic roles ----
	for (const [role, ref] of Object.entries(SEMANTIC_ROLES)) {
		const v = paletteHex(ref, colors);
		if (v) out.colors[role] = v;
		else console.warn(`SEMANTIC_ROLES: cannot resolve ${role} → ${ref}`);
	}

	// ---- colors: surface / on-surface (light + dark) ----
	const lightTheme = semanticColors.themes?.light ?? {};
	const darkTheme = semanticColors.themes?.dark ?? {};
	for (const [outKey, themeKey] of SURFACE_KEYS) {
		const ref = lightTheme[themeKey];
		if (!ref) continue;
		const hex = String(ref).startsWith('{') ? resolveColorRef(ref, colors) : ref;
		if (hex) out.colors[outKey] = hex;
	}
	out.colors['border-top'] = paletteHex('base-light', colors);

	for (const [outKey, themeKey] of SURFACE_KEYS) {
		const ref = darkTheme[themeKey];
		if (!ref) continue;
		const hex = String(ref).startsWith('{') ? resolveColorRef(ref, colors) : ref;
		if (hex) out.colors[outKey + '-dark'] = hex;
	}
	out.colors['border-top-dark'] = paletteHex('orange-900', colors);

	// ---- colors: accents (light/dark colors with subdued siblings) ----
	out.colors['accent-orange'] = paletteHex('orange-500', colors);
	out.colors['accent-blue'] = paletteHex('blue', colors);
	out.colors['accent-green'] = paletteHex('green', colors);
	out.colors['accent-blue-subdued'] = colors.blue?.subdued?.$value;
	out.colors['accent-green-subdued'] = colors.green?.subdued?.$value;
	out.colors['accent-red'] = paletteHex('red', colors);
	out.colors['accent-red-subdued'] = colors.red?.subdued?.$value;

	// ---- colors: full scales ----
	for (const k of ['100', '200', '250', '300', '400', '500', '600', '700', '800', '900']) {
		if (colors.gray?.[k]?.$value) out.colors[`gray-${k}`] = colors.gray[k].$value;
	}
	for (const k of ['100', '200', '300', '400', '500', '600', '700', '800', '900']) {
		if (colors.orange?.[k]?.$value) out.colors[`orange-${k}`] = colors.orange[k].$value;
	}
	for (const k of ['darkest', 'dark', 'light', 'lightest']) {
		if (colors[`base-${k}`]?.$value) out.colors[`base-${k}`] = colors[`base-${k}`].$value;
	}

	// ---- typography composites ----
	const stylesIn = typographyJson.styles ?? {};
	const styleNameMap = {'heading.1': 'heading-1', 'heading.2': 'heading-2', 'heading.3': 'heading-3'};

	function refKey(ref, prefix) {
		const m = String(ref).match(new RegExp(`^\\{${prefix}\\.([^}]+)\\}$`));
		return m ? m[1] : null;
	}

	for (const [styleKey, parts] of Object.entries(stylesIn)) {
		const outName = styleNameMap[styleKey] ?? styleKey;
		const composite = {};

		const ff = refKey(parts.fontFamily, 'font\\.family');
		if (ff && fonts[ff]) {
			composite.fontFamily = fonts[ff].penpot ?? (Array.isArray(fonts[ff].$value) ? fonts[ff].$value[0] : fonts[ff].$value);
		}
		const fs = refKey(parts.fontSize, 'font\\.size');
		if (fs && textSizes[fs]) composite.fontSize = fluidMaxToPx(textSizes[fs].$value);
		const fw = refKey(parts.fontWeight, 'font\\.weight');
		if (fw && textWeights[fw]) composite.fontWeight = textWeights[fw].$value;
		const lh = refKey(parts.lineHeight, 'font\\.lineHeight');
		if (lh && textLeading[lh]) composite.lineHeight = textLeading[lh].$value;

		out.typography[outName] = composite;
	}

	// Inject letterSpacing on body + headings (matches --tracking in
	// global-styles.css; ch is not a spec-allowed Dimension unit so the
	// closest equivalent in em is emitted).
	for (const k of ['body', 'heading-1', 'heading-2', 'heading-3']) {
		if (out.typography[k]) out.typography[k].letterSpacing = '-0.04em';
	}

	// label-caps and code styles aren't in typography.json — derive them
	// from the same atomic tokens so they stay in sync if those change.
	if (fonts.base) {
		out.typography['label-caps'] = {
			fontFamily: fonts.base.penpot ?? fonts.base.$value?.[0],
			fontSize: fluidMaxToPx(textSizes['step-min-1']?.$value),
			fontWeight: textWeights.bold?.$value,
			lineHeight: textLeading.flat?.$value,
			letterSpacing: '0.09em',
		};
	}
	if (fonts.mono) {
		out.typography['code'] = {
			fontFamily: fonts.mono.penpot ?? fonts.mono.$value?.[0],
			fontSize: fluidMaxToPx(textSizes['step-min-1']?.$value),
			fontWeight: textWeights.regular?.$value,
			lineHeight: textLeading.standard?.$value,
		};
	}

	// ---- rounded ----
	const roundedNameMap = {small: 'sm', medium: 'md'};
	out.rounded.none = '0px';
	for (const [k, v] of Object.entries(borderRadius)) {
		if (k.startsWith('$')) continue;
		out.rounded[roundedNameMap[k] ?? k] = v.$value;
	}
	out.rounded.full = '9999px';

	// ---- spacing ----
	out.spacing.base = fluidMaxToPx(textSizes['step-0']?.$value);
	for (const [k, v] of Object.entries(spacing)) {
		if (k.startsWith('$')) continue;
		out.spacing[k] = fluidMaxToPx(v.$value);
	}
	// Derived constants from variables.css — kept here so the spec stays
	// honest about what's actually in --gutter and --wrapper-width.
	out.spacing.gutter = fluidMaxToPx(spacing['m-l']?.$value);
	out.spacing.wrapper = '1360px';

	// ---- components ----
	out.components = COMPONENTS;

	return out;
}

// =============================================================================
// Emit
// =============================================================================

const FRONTMATTER_RE = /^---\n[\s\S]*?\n---\n?/;

const data = await build();

const yamlOut = yaml.dump(data, {
	quotingType: '"',
	forceQuotes: false,
	lineWidth: 100,
	noRefs: true,
	sortKeys: false,
});

let body = DEFAULT_BODY;
try {
	const existing = await readFile(OUTPUT_PATH, 'utf8');
	const stripped = existing.replace(FRONTMATTER_RE, '');
	body = stripped === existing ? '\n' + existing.replace(/^\n*/, '') : stripped.replace(/^\n*/, '');
} catch (e) {
	if (e.code !== 'ENOENT') throw e;
}

const finalText = '---\n' + yamlOut + '---\n\n' + body;
// docs/ is gitignored on this project, so it may not exist on a fresh
// clone. Create it before writing.
await mkdir(dirname(OUTPUT_PATH), {recursive: true});
await writeFile(OUTPUT_PATH, finalText, 'utf8');

// Validate the round trip
const parsed = yaml.load(yamlOut);
if (!parsed?.name || !parsed?.colors || !parsed?.typography) {
	console.error('Validation failed: front matter is missing required keys');
	process.exit(1);
}
// Confirm every {token.path} reference in the front matter resolves.
const allRefs = new Set();
(function walk(node) {
	if (typeof node === 'string') {
		for (const m of node.matchAll(/\{([^}]+)\}/g)) allRefs.add(m[1]);
		return;
	}
	if (node && typeof node === 'object') for (const v of Object.values(node)) walk(v);
})(parsed);
const broken = [];
for (const ref of allRefs) {
	const parts = ref.split('.');
	let cur = parsed;
	for (const p of parts) cur = cur && cur[p];
	if (cur === undefined) broken.push(ref);
}
if (broken.length) {
	console.error('Broken token references:', broken.join(', '));
	process.exit(1);
}

console.log(`Wrote ${OUTPUT_PATH.replace(REPO_ROOT + '/', '')}\n`);
console.log('Front-matter summary:');
console.log(`  colors:     ${Object.keys(parsed.colors).length} tokens`);
console.log(`  typography: ${Object.keys(parsed.typography).length} styles`);
console.log(`  rounded:    ${Object.keys(parsed.rounded).length} steps`);
console.log(`  spacing:    ${Object.keys(parsed.spacing).length} steps`);
console.log(`  components: ${Object.keys(parsed.components).length} entries`);
console.log(`  references: ${allRefs.size} resolved`);
console.log('\nMarkdown body preserved verbatim. Edit JSON tokens, not the YAML.');
